import fetch from 'node-fetch'

let timeout = 120000

let handler = async (m, { conn, usedPrefix }) => {
    conn.tebaklagu = conn.tebaklagu ? conn.tebaklagu : {}
    let id = m.chat
    
    if (id in conn.tebaklagu) {
        return conn.reply(m.chat, 'вҡ пёҸ Masih ada soal belum terjawab di chat ini!', conn.tebaklagu[id][0])
    }
    
    await m.react('вҸі')
    
    try {
        let res = await fetch('https://api.jagoanproject.com/api/game/tebaklagu', {
            headers: { 'Authorization': 'Bearer Lynxdecode' }
        })
        let json = await res.json()
        
        if (!json.status || !json.result) throw new Error('API Error')
        
        let data = json.result
        let poin = Math.floor(Math.random() * 100) + 1
        
        let caption = `в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *TEBAK LAGU* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\n`
        caption += `в”Ҡ рҹҢё *Penyanyi / Artis:* ${data.artis}\n`
        caption += `в”Ҡ вҸұпёҸ *Waktu:* ${(timeout / 1000)} detik\n`
        caption += `в”Ҡ рҹҺҒ *Hadiah:* ${poin} XP\n`
        caption += `в”Ҡ вҳҒпёҸ *Balas pesan ini untuk menjawab!*\n`
        caption += `в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§\n> рҹҢё *Li Shiya MD - Game* рҹҢё`
        
        // Kirim teksnya dulu biar bisa di-reply pengguna
        let msg = await m.reply(caption)
        
        // Baru kirim audionya dengan nge-quote pesan teks tadi
        await conn.sendMessage(m.chat, { audio: { url: data.lagu }, mimetype: 'audio/mpeg' }, { quoted: msg })
        
        conn.tebaklagu[id] = [
            msg,
            data,
            poin,
            setTimeout(() => {
                if (conn.tebaklagu[id]) {
                    conn.reply(m.chat, `в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *WAKTU HABIS* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ рҹҢё *Jawaban:* ${data.judul}\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§`, conn.tebaklagu[id][0])
                    delete conn.tebaklagu[id]
                }
            }, timeout)
        ]
        
        await m.react('вң…')
    } catch (err) {
        console.error(err)
        await m.react('вқҢ')
        m.reply(`в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *ERROR* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ вҡ пёҸ Gagal mengambil soal.\nв”Ҡ _${err.message || 'API Sedang Down'}_\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§`)
    }
}

handler.before = async function (m, { conn }) {
    conn.tebaklagu = conn.tebaklagu ? conn.tebaklagu : {}
    let id = m.chat
    
    if (!m.quoted || !m.text || !/TEBAK LAGU/i.test(m.quoted.text)) return false
    if (!(id in conn.tebaklagu)) return false
    
    if (m.quoted.id == conn.tebaklagu[id][0].id) {
        let json = conn.tebaklagu[id][1]
        if (m.text.toLowerCase().trim() === json.judul.toLowerCase().trim()) {
            global.db.data.users[m.sender].exp += conn.tebaklagu[id][2]
            
            let txt = `в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *BENAR!* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\n`
            txt += `в”Ҡ рҹҺү *Jawaban:* ${json.judul}\n`
            txt += `в”Ҡ рҹ‘Ө *Artis:* ${json.artis}\n`
            txt += `в”Ҡ рҹҺҒ *Bonus:* +${conn.tebaklagu[id][2]} XP\n`
            txt += `в•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§\n> рҹҢё *Li Shiya MD - Game* рҹҢё`
            
            m.reply(txt)
            clearTimeout(conn.tebaklagu[id][3])
            delete conn.tebaklagu[id]
        } else {
            m.reply('вқҢ *Salah!* Coba lagi.')
        }
        return true
    }
    return false
}

handler.help = ['tebaklagu']
handler.tags = ['game']
handler.command = /^(tebaklagu)$/i

export default handler
import fetch from 'node-fetch';

const handler = async (m, { conn, text, usedPrefix, command }) => {
    if (!text) {
        return m.reply(`в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *INFO* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ рҹҢё Masukkan pertanyaan atau teks untuk Groq AI!\nв”Ҡ вҳҒпёҸ Contoh: *${usedPrefix + command} apa yang kamu ketahui tentang Indonesia?*\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§`);
    }

    await m.react('вҸі');

    try {
        const apikey = 'x34J0';
        const model = 'llama-3.1-8b-instant';
        const apiUrl = `https://api.theresav.biz.id/ai/groq?q=${encodeURIComponent(text)}&model=${model}&apikey=${apikey}`;

        const response = await fetch(apiUrl);
        const json = await response.json();

        if (!json.status || !json.result) {
            await m.react('вқҢ');
            return m.reply('в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *ERROR* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ вҡ пёҸ Gagal mendapatkan respon dari Groq AI.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§');
        }

        // Mengirimkan hasil teks AI langsung ke user
        await m.reply(json.result);
        await m.react('вң…');

    } catch (e) {
        console.error(e);
        await m.react('вқҢ');
        m.reply('в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *ERROR* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ вҡ пёҸ Terjadi kesalahan saat memproses permintaan.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§');
    }
};

handler.help = ['groq <text>'];
handler.tags = ['ai'];

import fetch from 'node-fetch';

const handler = async (m, { conn, text, usedPrefix, command }) => {
    if (!text) {
        return m.reply(`в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *INFO* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ рҹҢё Masukkan teks atau pertanyaanmu!\nв”Ҡ вҳҒпёҸ Contoh: *${usedPrefix + command} halo bro apa kabar*\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§`);
    }

    await m.react('вҸі');

    try {
        conn.claude = conn.claude || {};
        const userId = m.sender;
        const sessionId = conn.claude[userId] || '';

        const apikey = 'x34J0';
        let apiUrl = `https://api.theresav.biz.id/ai/claude?text=${encodeURIComponent(text)}&apikey=${apikey}`;
        
        // Mengirimkan chatId jika sesi percakapan sebelumnya sudah ada agar obrolan berlanjut (continuous chat)
        if (sessionId) {
            apiUrl += `&chatId=${sessionId}`;
        }

        const response = await fetch(apiUrl);
        const json = await response.json();

        if (!json.status || !json.result) {
            await m.react('вқҢ');
            return m.reply('в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *ERROR* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ вҡ пёҸ Gagal mendapatkan respon dari Claude AI.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§');
        }

        // Menyimpan chatId untuk menjaga kelanjutan sesi percakapan user
        if (json.chatId) {
            conn.claude[userId] = json.chatId;
        }

        await m.reply(json.result);
        await m.react('вң…');

    } catch (e) {
        console.error(e);
        await m.react('вқҢ');
        m.reply('в•ӯв”Җв”Җ вӢҶ вң§ к’° рҹҺҖ *ERROR* рҹҺҖ к’ұ вң§ вӢҶ в”Җв”Җ\nв”Ҡ вҡ пёҸ Terjadi kesalahan saat memproses permintaan.\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ вӢҶ вң§');
    }
};

handler.help = ['claude <text>'];
handler.tags = ['ai'];
handler.command = /^claude$/i;
handler.limit = true;

export default handler;